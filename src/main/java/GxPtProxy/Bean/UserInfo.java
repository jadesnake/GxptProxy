package GxPtProxy.Bean;

public class UserInfo {
    private String  level; //用户级别
    private String  qylx;  //企业类型
    private String  qymc;  //企业名称
    private String  sbzq;  //申报周期
    private String  qysh;  //企业税号
    private String  oldsh; //旧税号
    private String  nsdq;  //纳税地区

    public String getLevel() {
        return level;
    }

    public void setLevel(String level) {
        this.level = level;
    }

    public String getQylx() {
        return qylx;
    }

    public void setQylx(String qylx) {
        this.qylx = qylx;
    }

    public String getQymc() {
        return qymc;
    }

    public void setQymc(String qymc) {
        this.qymc = qymc;
    }

    public String getSbzq() {
        return sbzq;
    }

    public void setSbzq(String sbzq) {
        this.sbzq = sbzq;
    }

    public String getQysh() {
        return qysh;
    }

    public void setQysh(String qysh) {
        this.qysh = qysh;
    }

    public String getOldsh() {
        return oldsh;
    }

    public void setOldsh(String oldsh) {
        this.oldsh = oldsh;
    }

    public String getNsdq() {
        return nsdq;
    }

    public void setNsdq(String nsdq) {
        this.nsdq = nsdq;
    }
}
